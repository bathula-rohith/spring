package com.spring.jdbc;

public class Student {
	 private int stroll;
	 private String stname;
	 private float stmarks;
	public int getStroll() {
		return stroll;
	}
	public void setStroll(int stroll) {
		this.stroll = stroll;
	}
	public String getStname() {
		return stname;
	}
	public void setStname(String stname) {
		this.stname = stname;
	}
	public float getStmarks() {
		return stmarks;
	}
	public void setStmarks(float stmarks) {
		this.stmarks = stmarks;
	}
	 

}
